    @staticmethod
    def _normalize_to_5_scale(value: float) -> float:
        """Normalize a score value to the 0.0-5.0 scale.

        Handles cases where the LLM returns percentages (e.g. 80, 300, 450)
        instead of the requested 0.0-5.0 scale.
        """
        if value > 5.0:
            # Likely a percentage (0-100) or scaled percentage (0-500)
            if value <= 100.0:
                value = value / 100.0 * 5.0
            elif value <= 500.0:
                # Could be 0-500 scale (percentage of 5.0)
                value = value / 100.0
            else:
                # Extremely high — treat as percentage
                value = value / 100.0 * 5.0
        return max(0.0, min(5.0, value))

    def _parse_score_response(
        self,
        axis: str,
        rubric: dict,
        response_text: str,
    ) -> dict:
        """Parse Claude's structured scoring response."""
        # Extract JSON from response — try multiple strategies
        text = response_text.strip()

        data = None

        # Strategy 1: Direct JSON parse
        try:
            data = json.loads(text)
        except json.JSONDecodeError:
            pass

        # Strategy 2: Extract from markdown code block
        if data is None and "```" in text:
            import re
            json_match = re.search(r'```(?:json)?\s*\n?(.*?)\n?```', text, re.DOTALL)
            if json_match:
                try:
                    data = json.loads(json_match.group(1).strip())
                except json.JSONDecodeError:
                    pass

        # Strategy 3: Find JSON object by balanced brace matching
        # Try every '{' position until we find valid JSON (handles leading text)
        if data is None:
            search_start = 0
            while search_start < len(text):
                start = text.find('{', search_start)
                if start < 0:
                    break
                depth = 0
                for i in range(start, len(text)):
                    if text[i] == '{':
                        depth += 1
                    elif text[i] == '}':
                        depth -= 1
                        if depth == 0:
                            try:
                                candidate = json.loads(text[start:i+1])
                                # Validate it looks like a score response (has score or details key)
                                if isinstance(candidate, dict) and ("score" in candidate or "details" in candidate):
                                    data = candidate
                            except json.JSONDecodeError:
                                pass
                            break
                if data is not None:
                    break
                search_start = start + 1

        if data is None:
            logger.error("Failed to parse LLM score response for axis %s. Full response (%d chars): %s", axis, len(text), text[:1000])
            return {
                "axis": axis,
                "score": 0.0,
                "confidence": 0.0,
                "details": [],
                "strengths": [],
                "risks": [f"LLM応答のパースに失敗しました: {text[:100]}"],
            }

        # Validate and normalize — all scores must be on 0.0-5.0 scale
        raw_score = float(data.get("score", 0.0))
        score = self._normalize_to_5_scale(raw_score)
        confidence = max(0.0, min(1.0, float(data.get("confidence", 0.0))))

        details = []
        for d in data.get("details", []):
            detail_score = float(d.get("score", 0.0))
            detail_score = self._normalize_to_5_scale(detail_score)
            # Sanitize evidence: replace internal/audit-style references
            evidence_text = d.get("evidence", "")
            if evidence_text:
                evidence_text = self._INTERNAL_PATTERN.sub("実際の出力", evidence_text)
                evidence_text = evidence_text.replace("スクリーンショット", "実際の出力")
                # Replace audit-inspection phrasing with consumer-friendly equivalents
                evidence_text = evidence_text.replace("確認された", "わかった")
                evidence_text = evidence_text.replace("確認できた", "わかった")
                evidence_text = evidence_text.replace("確認できる", "わかる")
                evidence_text = evidence_text.replace("観察される", "見られる")
                evidence_text = evidence_text.replace("表示されている", "記載されている")
            details.append({
                "rule_id": d.get("rule_id", "unknown"),
                "rule_name_jp": d.get("rule_name_jp", ""),
                "score": detail_score,
                "weight": float(d.get("weight", 1.0)),
                "evidence": evidence_text,
                "severity": d.get("severity", "medium"),
            })

        # CRITICAL FALLBACK: If the top-level score is 0 (or near-zero) but
        # detail scores exist with non-zero values, compute the axis score as
        # a weighted average of detail scores. This handles cases where the LLM
        # omits or zeroes the top-level "score" key but provides valid per-rule scores.
        if score < 0.01 and details:
            weighted_sum = sum(d["score"] * d["weight"] for d in details)
            total_weight = sum(d["weight"] for d in details)
            if total_weight > 0:
                fallback_score = weighted_sum / total_weight
                if fallback_score > 0.01:
                    logger.warning(
                        "Axis %s: top-level score=%.2f but detail weighted avg=%.2f — using detail average as axis score",
                        axis, score, fallback_score,
                    )
                    score = max(0.0, min(5.0, round(fallback_score, 2)))

        # Fallback: if LLM returned no details (or fewer than expected),
        # generate entries from the rubric criteria so the UI always has data.
        expected_rule_ids = {c["rule_id"] for c in rubric.get("criteria", [])}
        returned_rule_ids = {d["rule_id"] for d in details}
        missing = expected_rule_ids - returned_rule_ids

        if missing:
            logger.warning(
                "Axis %s: LLM returned %d/%d detail entries, generating %d fallback(s)",
                axis, len(details), len(expected_rule_ids), len(missing),
            )
            for c in rubric.get("criteria", []):
                if c["rule_id"] in missing:
                    details.append({
                        "rule_id": c["rule_id"],
                        "rule_name_jp": c["name_jp"],
                        "score": score,  # Use overall axis score as estimate
                        "weight": c["weight"],
                        "evidence": "（個別評価データなし。総合スコアを暫定適用）",
                        "severity": "info",
                    })

        # Extract language_detected for cross-axis penalty logic
        language_detected = data.get("language_detected", None)

        # Post-parse validation: if this is localization axis and language is "en",
        # enforce score cap even if LLM was too generous
        if axis == "localization" and language_detected == "en":
            if score > 1.5:
                logger.warning(
                    "Localization axis: LLM returned score %.1f but language_detected='en' — capping to 1.0",
                    score,
                )
                score = min(score, 1.0)
            # Also cap individual criteria that depend on Japanese text
            for d in details:
                if d["rule_id"] == "language_output" and d["score"] > 1.0:
                    d["score"] = min(d["score"], 1.0)
                elif d["rule_id"] in ("keigo_consistency", "business_expression", "readability", "terminology", "spoken_to_written"):
                    if d["score"] > 0.5:
                        d["score"] = 0.0
                        d["evidence"] += "（日本語テキストが存在しないため0.0）"

            # Recalculate weighted average
            weighted_sum = sum(d["score"] * d["weight"] for d in details)
            total_weight = sum(d["weight"] for d in details)
            if total_weight > 0:
                score = max(0.0, min(5.0, round(weighted_sum / total_weight, 2)))

        # Sanitize strengths/risks: remove internal/process-related items
        # that should not appear in consumer-facing reports.
        # Misclassified items are moved to the correct list.
        raw_strengths = data.get("strengths", [])
        raw_risks = data.get("risks", [])
        moved_to_risks: list[str] = []
        moved_to_strengths: list[str] = []
        strengths = self._sanitize_report_items(raw_strengths, is_strength=True, reclassified=moved_to_risks)
        risks = self._sanitize_report_items(raw_risks, is_strength=False, reclassified=moved_to_strengths)
        # Apply reclassifications
        if moved_to_risks:
            risks.extend(moved_to_risks)
        if moved_to_strengths:
            strengths.extend(moved_to_strengths)

        return {
            "axis": axis,
            "score": score,
            "confidence": confidence,
            "details": details,
            "strengths": strengths,
            "risks": risks,
            "language_detected": language_detected,
        }

    # Keywords that indicate internal/process language not suitable for consumer reports
    _INTERNAL_KEYWORDS = (
        "バッチ", "テスト完遂", "テスト完了率", "完遂率", "テスト数",
        "スコアリング", "スクリーンショット", "エビデンス",
        "観察", "API", "トークン", "コスト上限", "バジェット",
        "LLM", "パース", "データ不足", "評価不十分",
    )
    # Regex pattern for "テストN" or "検証N" references (e.g., "テスト9で", "検証12の")
    import re as _re
    _INTERNAL_PATTERN = _re.compile(r'(?:テスト|検証)\s*\d+')

    # Negative indicators that should NOT appear in strengths
    _NEGATIVE_MARKERS = (
        # Explicit negative phrases
        "注意が必要", "課題", "懸念", "不足", "改善が必要", "リスク", "問題",
        "確認を推奨", "推奨",
        # Limitation / insufficiency patterns
        "限定的", "限られ", "制限", "制約", "対応していない", "対応できない",
        "不向き", "不十分", "できない", "ない場合",
        "差異が小さい", "差別化が", "差が小さ", "差がな",
        # Negative adjectives / adverbs
        "欠け", "欠如", "欠点", "弱い", "弱点", "乏し", "低い",
        "遅い", "遅延", "長い",  # response time etc.
        "混在", "不一致", "一貫性に",
        # Conditional negatives
        "が必要", "を要する", "の余地", "に留まる", "にとどまる",
        "基本的な", "最低限", "標準的な",  # when describing limitations
    )

    # Positive indicators that should NOT appear in risks
    _POSITIVE_MARKERS = (
        "優れ", "高品質", "高い精度", "正確", "自然な", "充実",
        "豊富", "直感的", "使いやす", "迅速", "効率的",
        "対応している", "対応できる", "サポートして",
    )

    @classmethod
    def _sanitize_report_items(
        cls, items: list, is_strength: bool = False, *, reclassified: list | None = None,
    ) -> list[str]:
        """Filter out internal/process-related items from consumer-facing report.

        Detects misclassified items: negative statements in strengths are moved
        to the reclassified list (if provided) so the caller can add them to risks.
        """
        if not items or not isinstance(items, list):
            return []

        sanitized = []
        for item in items:
            if not isinstance(item, str) or not item.strip():
                continue
            text = item.strip()

            # Filter out items containing internal keywords
            if any(kw in text for kw in cls._INTERNAL_KEYWORDS):
                logger.debug("Filtered internal item from %s: %s",
                            "strengths" if is_strength else "risks", text[:80])
                continue

            # Filter out items with "テストN" or "検証N" references
            if cls._INTERNAL_PATTERN.search(text):
                logger.debug("Filtered test-reference item from %s: %s",
                            "strengths" if is_strength else "risks", text[:80])
                continue

            # Detect misclassified strengths (negative content in strengths list)
            if is_strength and any(neg in text for neg in cls._NEGATIVE_MARKERS):
                logger.info("Reclassified negative item from strengths→risks: %s", text[:100])
                if reclassified is not None:
                    reclassified.append(text)
                continue

            # Detect misclassified risks (positive content in risks list)
            if not is_strength and any(pos in text for pos in cls._POSITIVE_MARKERS):
                # Only reclassify if clearly positive and contains no negative markers
                if not any(neg in text for neg in cls._NEGATIVE_MARKERS):
                    logger.info("Reclassified positive item from risks→strengths: %s", text[:100])
                    if reclassified is not None:
                        reclassified.append(text)
                    continue

            # Replace audit-inspection phrasing with consumer-friendly equivalents
            text = text.replace("確認された", "わかった")
            text = text.replace("確認できた", "わかった")
            text = text.replace("確認できる", "わかる")
            text = text.replace("観察される", "見られる")
            text = text.replace("表示されている", "記載されている")
            text = cls._INTERNAL_PATTERN.sub("実際の出力", text)
            text = text.replace("スクリーンショット", "実際の出力")
            sanitized.append(text)

        return sanitized
